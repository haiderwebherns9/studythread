function get_preferred_languages($code="") {
	$preferred_languages_array = array();
	for ($i = 1; $i <= 10; $i++) {
		$language_x = 'wpjobster_language_' . $i;
		$wpjobster_language_x = trim(get_option($language_x));

		if ($wpjobster_language_x) {
			$preferred_languages_array[$wpjobster_language_x] = get_all_languages($wpjobster_language_x);
		}
	}

	if ($code) {
		return $preferred_languages_array[$code];
	} else {
		return $preferred_languages_array;
	}
}

