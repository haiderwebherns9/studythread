function tmp_update_jobs_country() {

	$the_query = new WP_Query( array('post_type' => 'job', 'posts_per_page' => -1) );

	if ( $the_query->have_posts() ) {
		echo '<ul>';
		while ( $the_query->have_posts() ) {
			$the_query->the_post();
			echo '<li>' . get_the_title() . '</li>';

			$author_country = get_the_author_meta('country_code');
			echo '<p>***' . get_post_meta(get_the_ID(), 'country_code', true) . '</p>';

			if ($author_country) {
				echo $author_country;

				// THE UPDATE!!!
				update_post_meta(get_the_ID(), 'country_code', $author_country);
			} else {
				echo 'NOT';
			}
		}
		echo '</ul>';
	} else {
		// no posts found
	}
	wp_reset_postdata();
}

