function wpj_generate_language_select() {
	// based on qtranxf_generateLanguageSelectCode();
	global $q_config;

	if ( is_404() ) {
		$url = get_option('home');
	} else {
		$url = '';
	}
	?>
	<div class="ui floating dropdown labeled icon basic button wpj-language-switcher">
		<i class="world icon"></i>
		<div class="default text">
			<?php echo $q_config['language_name'][$q_config['language']]; ?>
		</div>
		<?php
		echo PHP_EOL.'<div class="menu">'.PHP_EOL;

		foreach(qtranxf_getSortedLanguages() as $language) {
			$alt = $q_config['language_name'][$language].' ('.$language.')';

			if ( $language == $q_config['language'] ) {
				$extra_class = " active selected";
			} else {
				$extra_class = "";
			}

			if ( $language == 'en' ) {
				$flag = 'us';
			} else {
				$flag = $language;
			}

			echo '<a href="'.qtranxf_convertURL($url, $language, false, true).'"';
			echo ' class="item'.$extra_class.'" title="'.$alt.'">';
			echo '<i class="' . $flag . ' flag"></i>';
			echo $q_config['language_name'][$language].'</a>'.PHP_EOL;
		}

		echo '</div>'.PHP_EOL;
		?>
	</div>
	<?php
}

